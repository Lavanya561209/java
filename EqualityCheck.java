 class EqualityCheck {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;
        int z = 10;

        if (x == y) { 
            System.out.println("x and y are equal.");
        } else {
            System.out.println("x and y are not equal.");
        }

        if (x == z) {
            System.out.println("x and z are equal.");
        } else {
            System.out.println("x and z are not equal.");
        }
    }
}